</div><div class="main-page-content">
<h2 class="h2" name="">Privately rented accommodation</h2>
</div><div class="main-page-content" id="">
 <p>Lots of RCM students, particularly after their first year, choose to live in privately rented accommodation. London has a large rental market offering a range of properties in locations across the city.</p>
<p>During the year our Student Services team produces a list of landlords offering private accommodation to RCM students.</p>
</div><div class="main-page-content" id="">
 <p>With effect from 20 February 2016 private landlords will be required to check their tenants' immigration status before granting a tenancy agreement. This to make sure the tenant has valid permission to be in the UK.</p>
<p>To be eligible you must meet one of the following conditions:</p>
<ul>
<li>You are an EEA/Swiss national.</li>
<li>You have the right to be in the UK under EEA law (for example because you are the family member of an EEA/Swiss national).</li>
<li>You have valid immigration permission to be in the UK.</li>
</ul>
<p>The landlord may need to see your passport and/or visa to verify your immigration status.</p>
</div><div class="main-page-content" id="">
 <p>If you would like more information on how the RCM can provide support for students seeking privately rented accommodation, please feel free to talk to the Student Services team.</p>
