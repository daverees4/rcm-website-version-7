 <h3>Location</h3>
<p>Prince Consort&nbsp;Village is located in leafy south-west London and within easy travelling distance of the RCM. It takes around 40 minutes door-to-door using the London Underground between&nbsp;Ravenscourt Park and South Kensington.</p>
<p>The neighbourhood is ideal, offering the greenery of nearby&nbsp;<strong>Ravenscourt Park</strong>&nbsp;as well as the convenience of many&nbsp;local shops, cafes and amenities. Prince Consort Village is within easy reach of Westfield in <strong>Shepherd’s Bush</strong>, which houses leading fashion, home&nbsp;and lifestyle brands alongside cinemas and a fantastic&nbsp;selection of places to eat. Notting&nbsp;Hill – famous for&nbsp;<strong>Notting Hill Carnival</strong>&nbsp;as well as Portobello Road&nbsp;and its market – is only a short bus ride away. This part of London is arty and bohemian,&nbsp;with lots of restaurants, bars, shops and music venues.</p>
