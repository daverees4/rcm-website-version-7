<h3 class="h3" name="">Facilities</h3>
</div><div class="main-page-content" id="">
 <p>Prince Consort Village is fully equipped and specially&nbsp;designed for RCM students, with&nbsp;acoustically treated&nbsp;bedrooms&nbsp;allowing you to practise at home. Services&nbsp;include:</p>
<ul>
<li>23 dedicated practice rooms available 24/7</li>
<li>A&nbsp;film room</li>
<li>Open mic space</li>
<li>High speed Wi-Fi</li>
<li>Laundry</li>
<li>Reception</li>
<li>Gym</li>
<li>Bicycle storage</li>
<li>Indoor and outdoor&nbsp;communal spaces</li>
</ul>
